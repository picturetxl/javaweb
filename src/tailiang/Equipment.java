package tailiang;

public class Equipment {
    private String equid;
    private String equname;
    private String equprice;
    private String typeid;
    private String typename;
    private String supid;
    private String supaddr;
    private String supman;
    private String suptel;
    private String supname;
    public String getEquid() {
        return equid;
    }
    public void setEquid(String equid) {
        this.equid = equid;
    }
    public String getEquname() {
        return equname;
    }
    public void setEquname(String equname) {
        this.equname = equname;
    }
    public String getEquprice() {
        return equprice;
    }
    public void setEquprice(String equprice) {
        this.equprice = equprice;
    }
    public String getTypeid() {
        return typeid;
    }
    public void setTypeid(String typeid) {
        this.typeid = typeid;
    }
    public String getTypename() {
        return typename;
    }
    public void setTypename(String typename) {
        this.typename = typename;
    }
    public String getSupid() {
        return supid;
    }
    public void setSupid(String supid) {
        this.supid = supid;
    }
    public String getSupaddr() {
        return supaddr;
    }
    public void setSupaddr(String supaddr) {
        this.supaddr = supaddr;
    }
    public String getSupman() {
        return supman;
    }
    public void setSupman(String supman) {
        this.supman = supman;
    }
    public String getSuptel() {
        return suptel;
    }
    public void setSuptel(String siptel) {
        this.suptel = siptel;
    }
    public String getSupname() {
        return supname;
    }
    public void setSupname(String supname) {
        this.supname = supname;
    }
}

