package tailiang;

/**
 * Teacher
 */
public class Teacher {

    private String tech_id;
    private String tech_name;
    private String tech_password;
    private String tech_phone;


    /**
     * @return the tech_id
     */
    public String getTech_id() {
        return tech_id;
    }
    /**
     * @return the tech_name
     */
    public String getTech_name() {
        return tech_name;
    }
    /**
     * @return the tech_password
     */
    public String getTech_password() {
        return tech_password;
    }
    /**
     * @return the tech_phone
     */
    public String getTech_phone() {
        return tech_phone;
    }

    /**
     * @param tech_id the tech_id to set
     */
    public void setTech_id(String tech_id) {
        this.tech_id = tech_id;
    }


    /**
     * @param tech_name the tech_name to set
     */
    public void setTech_name(String tech_name) {
        this.tech_name = tech_name;
    }
    /**
     * @param tech_password the tech_password to set
     */
    public void setTech_password(String tech_password) {
        this.tech_password = tech_password;
    }
    /**
     * @param tech_phone the tech_phone to set
     */
    public void setTech_phone(String tech_phone) {
        this.tech_phone = tech_phone;
    }
}